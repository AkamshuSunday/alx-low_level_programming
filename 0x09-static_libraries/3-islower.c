/**
 * isLower - Checks if a character is lowercase.
 *
 * This function checks if the input character 'ch' is a lowercase letter.
 *
 * @param ch: The character to check.
 *
 * @return: Returns 1 if 'ch' is lowercase, otherwise returns 0.
 */
int isLower(int ch)
{
    return (ch >= 'a' && ch <= 'z');
}

